package domain;

public class MaterialInputType {

    private Long materialInputTypeId;
    private String name;

    public MaterialInputType() {
    }

    public Long getMaterialInputTypeId() {
        return materialInputTypeId;
    }

    public void setMaterialInputTypeId(Long materialInputTypeId) {
        this.materialInputTypeId = materialInputTypeId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
