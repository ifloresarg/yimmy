public class WoodType {

    private Float woodTypeId;
    private String name;

    public WoodType() {
    }

    public Float getWoodTypeId() {
        return woodTypeId;
    }

    public void setWoodTypeId(Float woodTypeId) {
        this.woodTypeId = woodTypeId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
